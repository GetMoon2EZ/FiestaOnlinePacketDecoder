static void show_debug_info(FOPDData *data)
{
    struct MemoryInfo mem_info = get_memory_info();
    char total_mem[MEMORY_DISPLAY_BUFFER_SIZE] = { 0 };
    char used_mem[MEMORY_DISPLAY_BUFFER_SIZE] = { 0 };
    char used_mem_proc[MEMORY_DISPLAY_BUFFER_SIZE] = { 0 };
    uint32_t ping = data->getPing();

    ImGui::Begin("Debug info");

    // Display frame rate
    ImGui::Text("%.3f ms/frame (%.1f FPS)", 1000.0f / ImGui::GetIO().Framerate, ImGui::GetIO().Framerate);
    if (ping == PING_ERROR) {
        ImGui::Text("Latency: N/A");
    } else {
        ImGui::Text("Latency: %d ms", ping);
    }

    ImGui::SeparatorText("Memory usage");
    memory_display(mem_info.totalPhysMem, total_mem);
    memory_display(mem_info.physMemUsed, used_mem);
    memory_display(mem_info.physMemUsedByProcess, used_mem_proc);
    ImGui::Text("Physical Memory: %s / %s / %s", used_mem_proc, used_mem, total_mem);

    memory_display(mem_info.totalVirtualMem, total_mem);
    memory_display(mem_info.virtualMemUsed, used_mem);
    memory_display(mem_info.virtualMemUsedByProcess, used_mem_proc);
    ImGui::Text("Virtual Memory : %s / %s / %s", used_mem_proc, used_mem, total_mem);
    ImGui::End();
}

static void show_parameters(FOPDData *data)
{
    /* Server selection */
    ImGui::Begin("Parameters");

    const char* server_names[SERVER_COUNT] = { 0 };
    for (size_t i = 0; i < SERVER_COUNT; i++) {
        server_names[i] = fo_servers[i].name;
    }

    size_t item_current = data->getServerIndex();
    const char* combo_preview_value = server_names[item_current];  // Pass in the preview value visible before opening the combo (it could be anything)

    if (ImGui::BeginCombo("Server", combo_preview_value, 0)) {
        for (size_t n = 0; n < IM_ARRAYSIZE(server_names); n++) {
            const bool is_selected = (item_current == n);
            if (ImGui::Selectable(server_names[n], is_selected)) {
                if (!is_selected) {
                    std::cout << "[DEBUG] Server value changed" << std::endl;
                    /* Change sniffing filter */
                }
                item_current = n;
            }

            // Set the initial focus when opening the combo (scrolling + keyboard navigation focus)
            if (is_selected) {
                ImGui::SetItemDefaultFocus();
            }
        }
        data->setServerIndex(item_current);
        ImGui::EndCombo();
    }

    ImGui::Text("Server address: %s", fo_servers[item_current].address);
    ImGui::End();
}

static void plot_dps_over_time(FOPDData *data)
{
    double now = (double) time(NULL);

    ImGui::Begin("Plots");

    if (ImGui::Button("Reset")) {
        start = (double) time(NULL) - 1;
        dps.clear();
        avg_dps.clear();
        rolling_avg_dps.clear();
        time_axis.clear();
        dps_plot_timeout = 0;
    }

    if (dps_plot_timeout == 0) {
        dps.push_back((double) data->getDPS());
        avg_dps.push_back(data->getDPSAverage());
        rolling_avg_dps.push_back(data->getDPSRollingAverage());
        time_axis.push_back(now);
    }
    dps_plot_timeout = (dps_plot_timeout + 1) % PLOT_POLL_TIME;

    if (ImPlot::BeginPlot("Damage Graph")) {
        double dps_axis_limit = (double) data->getMaxDPS() ? data->getMaxDPS() * 1.1 : 1000;
        ImPlot::SetupAxis(ImAxis_X1, "Time");
        ImPlot::SetupAxis(ImAxis_Y1, "Damage");
        ImPlot::SetupAxisScale(ImAxis_X1, ImPlotScale_Time);
        ImPlot::SetupAxisLimits(ImAxis_X1, start, now, ImPlotCond_Always);
        ImPlot::SetupAxisLimits(ImAxis_Y1, 0, dps_axis_limit, ImPlotCond_Always);
        ImPlot::SetupFinish();

        ImPlot::PlotLine("##dps", &time_axis[0], &dps[0], static_cast<int>(dps.size()));
        ImPlot::PlotLine("##average_dps", &time_axis[0], &avg_dps[0], static_cast<int>(avg_dps.size()));
        ImPlot::PlotLine("##rolling_average_dps", &time_axis[0], &rolling_avg_dps[0], static_cast<int>(rolling_avg_dps.size()));

        ImPlot::EndPlot();
    }

    ImGui::End();
}

static void show_friends(FOPDData *data)
{
    std::vector<struct friend_info*> v = data->getFriendInfos();

    ImGui::Begin("Players");

    if (ImGui::BeginTable("player_info", 5, ImGuiTableFlags_BordersInnerH | ImGuiTableFlags_SizingFixedFit)) {
        ImGui::TableSetupColumn("Name");
        ImGui::TableSetupColumn("Class");
        ImGui::TableSetupColumn("Level");
        ImGui::TableSetupColumn("Map");
        ImGui::TableSetupColumn("Last update");
        ImGui::TableHeadersRow();

        if (v.empty()) {
            ImGui::TableNextColumn();
            ImGui::Text("Click on \"Find friends\"");
        }

        for (struct friend_info *finfo : v) {
            char since[TIME_SINCE_BUFFER_SIZE] = { 0 };

            time_since_str(finfo->last_seen, since);

            ImGui::TableNextColumn();
            ImGui::Text("%s", finfo->name);
            ImGui::TableNextColumn();
            ImGui::Text("%s", translate_pclass(finfo->pclass));
            ImGui::TableNextColumn();
            ImGui::Text("%u", finfo->level);
            ImGui::TableNextColumn();
            ImGui::Text("%s", translate_map_name(finfo->raw_map));
            ImGui::TableNextColumn();
            ImGui::Text("%s", since);
        }

        ImGui::EndTable();
    }

    ImGui::End();
}

void build_gui(void)
{
    // Uses ImGUI to display some information
    // The data is stored in a singleton
    // The singleton can be polled using one of its methods
    // Making it thread safe should not be too hard :)

    FOPDData *data = FOPDData::getInstance();

    show_debug_info(data);

    show_parameters(data);

    show_friends(data);
    // ImGui::ShowDemoWindow();

    /* Damage statistics */
    ImGui::SetNextWindowBgAlpha(0.35f); // Transparent background
    ImGui::Begin("DPS meter");
    ImGui::Text("Target health: %d", data->getTargetRemainingHealth());
    ImGui::Text("Max dmg    : %u", data->getMaxDmg());
    ImGui::Text("Max DPS    : %u", data->getMaxDPS());
    ImGui::Text("Current DPS: %u", data->getDPS());
    ImGui::Text("Average DPS: %.2f", data->getDPSAverage());
    ImGui::End();

    plot_dps_over_time(data);
}

